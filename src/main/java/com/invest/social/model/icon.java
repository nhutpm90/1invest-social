package com.invest.social.model;

public class icon {
	private String topbar;
	private String smaller;
	private String small;
	private String larger;
	private String large;

	public String getTopbar() {
		return topbar;
	}
	public void setTopbar(String topbar) {
		this.topbar = topbar;
	}
	public String getSmaller() {
		return smaller;
	}
	public void setSmaller(String smaller) {
		this.smaller = smaller;
	}
	public String getSmall() {
		return small;
	}
	public void setSmall(String small) {
		this.small = small;
	}
	public String getLarger() {
		return larger;
	}
	public void setLarger(String larger) {
		this.larger = larger;
	}
	public String getLarge() {
		return large;
	}
	public void setLarge(String large) {
		this.large = large;
	}
}
